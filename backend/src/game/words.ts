/**
 * 겹치지 않는 단어 count개를 무작위로 뽑는다(부분 Fisher-Yates).
 * 뱅크가 count보다 작으면 있는 만큼 반환한다.
 */
export function pickChoices(count: number): string[] {
  const pool = [...new Set(WORDS)]; // 혹시 모를 중복 제거 → 같은 단어가 두 번 뽑히지 않게
  const n = Math.min(count, pool.length);
  for (let i = 0; i < n; i++) {
    const j = i + Math.floor(Math.random() * (pool.length - i));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }
  return pool.slice(0, n);
}
